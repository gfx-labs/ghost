func TestEncodeBigIntRoundtrip(t *testing.T) {
	values := []*big.Int{
		big.NewInt(0),
		big.NewInt(1),
		big.NewInt(-1),
		big.NewInt(12345),
		big.NewInt(-12345),
		big.NewInt(256),
		big.NewInt(-256),
	}
	for _, v := range values {
		b := &Builder{}
		b.BigInt(v)
		encoded := b.Finish()
		dec := NewDecoder(encoded)
		decoded, err := dec.BigInt()
		assert.NoError(t, err, "value: %v", v)
		assert.Equal(t, 0, v.Cmp(decoded), "expected %v, got %v", v, decoded)
	}
}

